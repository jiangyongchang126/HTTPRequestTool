@implementation UIDevice (Resolutions)

/******************************************************************************
 函数名称 : + (UIDeviceResolution) currentResolution
 函数描述 : 获取当前分辨率
 输入参数 : N/A
 输出参数 : N/A
 返回参数 : N/A
 备注信息 :
 ******************************************************************************/
+ (UIDeviceResolution) currentResolution {
    if(UI_USER_INTERFACE_IDIOM() == UIUserInterfaceIdiomPhone){
        if ([[UIScreen mainScreen] respondsToSelector: @selector(scale)]) {
            
            CGSize result = [[UIScreen mainScreen] bounds].size;
            result = CGSizeMake(result.width * [UIScreen mainScreen].scale, result.height * [UIScreen mainScreen].scale);
            NSLog(@"111111111111111-%f   2222222222222-%f",result.height,result.width);
            if (result.height <= 480.0f)
                return UIDevice_iPhoneStandardRes;
            else if(result.height >= 960.0f && result.height < 1137.0f)
                return UIDevice_iPhoneHiRes;  //iPhone 5 高清分辨率(640x1136px)
            else if(result.height >= 1137.0f && result.height < 1335.0f)
                return UIDevice_iPhoneTallerHiRe6;
            else if(result.height >= 1335.0f && result.height < 2210.0f)
                return UIDevice_iPhoneTallerHiRe6p;
            else if(result.height >= 2211.0f && result.height < 2437.0f)
                return UIDevice_iPhoneTallerHiReX;
            return UIDevice_iPhoneTallerHiRes;
        } else
            return UIDevice_iPhoneStandardRes;
    } else
        return (([[UIScreen mainScreen] respondsToSelector: @selector(scale)]) ? UIDevice_iPadHiRes : UIDevice_iPadStandardRes);
}

/******************************************************************************
 函数名称 : + (BOOL)isRunningOniPhone
 函数描述 : 当前是否运行在iPhone端
 输入参数 : N/A
 输出参数 : N/A
 返回参数 : N/A
 备注信息 :
******************************************************************************/

+ (BOOL)isRunningOniPhone4s{
    if ([self currentResolution] == UIDevice_iPhoneHiRes) {
        return YES;
    }
    return NO;
}
+ (BOOL)isRunningOniPhone5{
    if ([self currentResolution] == UIDevice_iPhoneTallerHiRes) {
        return YES;
    }
    return NO;
}
+ (BOOL)isRunningOniPhone6{
    if ([self currentResolution] == UIDevice_iPhoneTallerHiRe6) {
        return YES;
    }
    return NO;
}
+ (BOOL)isRunningOniPhone6p{
    if ([self currentResolution] == UIDevice_iPhoneTallerHiRe6p) {
        return YES;
    }
    return NO;
}
+ (BOOL)isRunningOniPhoneX{
    if ([self currentResolution] == UIDevice_iPhoneTallerHiReX) {
        return YES;
    }
    return NO;
}

